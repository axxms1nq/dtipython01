# คำสั่งแสดงผลข้อมูลใดๆ ที่หน้าจอ ใช้ฟังชก์ชัน print()
# string ข้อความ
print('Hello')
print('Hi')
print('''Hey''')
print("""Hum""")

# number ตัวเลข
print(48654)   #integer/int
print(15.4568) #float

# boolean ตรรกะ มีค่า True หรือ False
print(True)   
print(False)

# expression นิพจน์ คือ อะไรก็ตามที่มี process การทำงาน เช่น การคำนวณ การเปรียบเทียบ 
print(10 + 20)
print(50 > 90)
print('Bangna' <= 'Bangpoo')
